
public class LiteralDisplay {
    public static void main(String[] args) {
        // display an int literal
        System.out.print("This is the integer literal 123: ");
        System.out.println(123);
        
        // display a long literal
        System.out.print("This is the long literal 9876543210L: ");
        System.out.println(9876543210L);

        // display a float literal
        System.out.print("This is the float literal 123.456789F: ");
        System.out.println(123.456789F);

        // display a double literal
        System.out.print("This is the double literal 123.456789: ");
        System.out.println(123.456789);

        // display a character literal
        System.out.print("This is the character literal 'a': ");
        System.out.println('a');

        // display a boolean literal
        System.out.print("This is the boolean literal true: ");
        System.out.println(true);

        // display a binary literal
        System.out.print("This is the binary literal 0b00110011: ");
        System.out.println(0b00110011);

        // display a hexadecimal literal
        System.out.print("This is the hex literal 0x10FACE: ");
        System.out.println(0x10FACE);
    }
}
